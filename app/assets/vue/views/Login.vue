<template>
    <div>
      <div class="login-container">
        <el-form autoComplete="on" :model="loginForm" :rules="loginRules" ref="loginForm" label-position="left" label-width="0px"
                 class="card-box login-form">
            <h3 class="title">Please login into:</h3>
            <el-form-item prop="username">
                <span class="svg-container svg-container_login">
                  <i class="fas fa-user"></i>
                </span>
                <el-input name="username"
                          type="text"
                          v-model="loginForm.login"
                          autoComplete="on"
                          placeholder="login"
                />
            </el-form-item>
            <el-form-item prop="password">
                <span class="svg-container">
                  <i class="fas fa-key"></i>
                </span>
                <el-input name="password"
                          :type="pwdType"
                          @keyup.enter.native="handleLogin"
                          v-model="loginForm.password"
                          autoComplete="on"
                          placeholder="password"
                />
                <span class="show-pwd" @click="showPwd"><i class="fas fa-eye"></i></span>
            </el-form-item>
            <el-form-item>
                <el-button type="primary" style="width:100%;" :loading="loading" :disabled="loginForm.login.length === 0 || loginForm.password.length === 0 || isLoading" @click.native.prevent="handleLogin">
                    Sign in
                </el-button>
            </el-form-item>
            <div class="tips">
                <span style="margin-right:20px;">username: admin</span>
                <span> password: admin</span>
            </div>
        </el-form>
      </div>
      <div v-if="isLoading" class="row col">
          <p>Loading...</p>
      </div>

      <div v-else-if="hasError" class="row col">
          <error-message :error="error"></error-message>
      </div>
    </div>
</template>

<script>
    import ErrorMessage from '../components/ErrorMessage';

    export default {
        name: 'login',
        components: {
            ErrorMessage,
        },
        data() {
            const validateUsername = (rule, value, callback) => {
                if (value.length < 1) {
                    callback(new Error('Username is required'))
                } else {
                    callback()
                }
            };
            const validatePass = (rule, value, callback) => {
                if (value.length < 5) {
                    callback(new Error('The password can not be less than 5 digits'))
                } else {
                    callback()
                }
            };
            return {
                loginForm: {
                    login: 'admin',
                    password: 'admin'
                },
                loginRules: {
                    login: [{ required: true, trigger: 'blur', validator: validateUsername }],
                    password: [{ required: true, trigger: 'blur', validator: validatePass }]
                },
                loading: false,
                pwdType: 'password'
            }
        },
        computed: {
            isLoading () {
                return this.$store.getters['security/isLoading'];
            },
            hasError () {
                return this.$store.getters['security/hasError'];
            },
            error () {
                return this.$store.getters['security/error'];
            },
        },
        created() {
            let redirect = this.$route.query.redirect;

            if (this.$store.getters['security/isAuthenticated']) {
                if (typeof redirect !== 'undefined') {
                    this.$router.push({path: redirect});
                } else {
                    this.$router.push({path: '/home'});
                }
            }
        },
        methods: {
            showPwd() {
                if (this.pwdType === 'password') {
                    this.pwdType = ''
                } else {
                    this.pwdType = 'password'
                }
            },

            handleLogin() {
                this.$refs.loginForm.validate(async (valid) => {
                    if (valid === false) {
                        console.log('error submit!!');
                        return false;
                    }

                    this.loading = true;
                    let redirect = this.$route.query.redirect;

                    try {
                        await this.$store.dispatch('security/login', this.loginForm);
                        this.loading = false;

                        if (!this.$store.getters['security/hasError']) {
                            if (typeof redirect !== 'undefined') {
                                this.$router.push({path: redirect});
                            } else {
                                this.$router.push({path: '/home'});
                            }
                        }
                    } catch (error) {
                        this.loading = false;
                    }
                })
            }
        }
    }
</script>

